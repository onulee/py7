number = 102.123456789
# 소수점 1자리까지만 출력하시오.
print("%.1f" % number)

number2 = 1.2345
# 소수점 2자리까지 출력하시오.
print("%.2f" % number2)

number3 = 100        # 정수 %d
# 10자리 공간을 확보해서 빈공백 0으로 채워 출력하시오.
print("%010d" % number3)

number4 = 1000.236  # 실수 %f
# 10자리 공간을 확보해서 빈공백 0으로, 소수점은 2자리까지 출력
print("%010.2f" % number4)
